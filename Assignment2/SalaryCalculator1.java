    private class RadioListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            if (buttonGroup1.getSelection() == PartTimeRB.getModel()) {
                flexiLabel.setVisible(true);
                FlexiTF.setVisible(true);
            } else if (buttonGroup1.getSelection() == FullTimeRB.getModel()) {
                flexiLabel.setVisible(false);
                FlexiTF.setVisible(false);
            }
        }
    }

