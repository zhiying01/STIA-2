    private void CalculateBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateBTNActionPerformed
        String name = NameTF.getText();
        String id = IDTF.getText();
        int months = Integer.parseInt(DurationTF.getText());
        double salary = 0;
        double bonus = 0;
        double totalSalary = 0;

        if (PartTimeRB.isSelected()) {
            int hoursWorked = Integer.parseInt(FlexiTF.getText());
            double hourlyRate = 15.0;
            salary = hoursWorked * hourlyRate;

            PartTimeEmployee employee = new PartTimeEmployee(id, name, months, hoursWorked);
            employee.calculateSalary();
            bonus = employee.calculateBonus(salary);
            totalSalary = salary + bonus;
        } else if (FullTimeRB.isSelected()) {
            double basicSalary = Double.parseDouble(FlexiTF.getText());
            salary = basicSalary;

            FullTimeEmployee employee = new FullTimeEmployee(id, name, months, basicSalary);
            employee.calculateSalary();
            bonus = employee.calculateBonus(salary);
            totalSalary = salary + bonus;
        }
        String output = "Name: " + name + "\nEmployee ID: " + id + "\nEmployment Duration (months): " + months
                + "\nMonthly Salary: RM " + salary + "\nBonus: RM " + bonus + "\nTotal Salary: RM " + totalSalary;
        OutputArea.setText(output);
    }//GEN-LAST:event_CalculateBTNActionPerformed

